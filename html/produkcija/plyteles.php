Plytelės <br><br>
Jos gali būti glazūruotos, lygaus ar pašiurkštinto paviršiaus (atsparios slydimui), poliruotos ar pusiau poliruotos. Tinka intensyvaus judėjimo patalpose, jos yra atsparios trinčiai, temperatūrų kaitai ir yra ilgaamžės. Taip pat puikiai tinka šildomoms grindims, kadangi yra itin laidžios šilumai. <br><br>

<b>Lauke naudojamos akmens masės plytelės</b> <br><br>
                
Jos yra atsparios šalčiui, temperatūrų kaitai ir yra mažo įmirkio. Jei plytelė yra glazūruota, toks paviršius turi būti atsparus trūkinėjimams. Geriausiai naudoti 1cm arba 2cm storio plyteles. Paviršius turi būti neslidus arba pašiurkštintas.<br><br>

Čia rasite tik dalį plytelių kolekcijų, kurias gali pasiūlyti LINEA. Visada maloniai kviečiame užsukti į artimiausią mūsų saloną ir pasitarti su konsultantu.<br><br>

Pirkėjas reikiamų plytelių kiekį turetų pasiskaičiuoti su atsarga. Visada savo klientams mes patariame pirkti 10% plytelių daugiau, negu reikia. Klijuojant paprastai patiriama 5% nuostolių, jei plyteles klijuosite įstrižai – 7 %. Likusi atsarga pravers smulkiems remonto darbams ateityje.<br><br>

Atspalvis gali skirtis nuo parduotuvėje eksponuojamų pavyzdžių. Atspalvis (gamintojų vadinamas „tonu“) – tai konkrečios gaminamų plytelių partijos spalvinis niuansas. Dėl gamybos technologijos ypatumų vienos gaminamos serijos plytelės spalvos tonu gali skirtis nuo nominalios spalvos (pvz., žalios) – t.y. gali būti šviesesnės arba tamsesnės. Tokie plytelių atspalvio svyravimai būdingi visoms plytelėms ir jų dekorams, kurios gaminamos aukštos temperatūros krosnyse. Parduotuvėje eksponuojami pavyzdžiai ir gamyklos sandėliuose esančios prekės bus iš skirtingų gamybos partijų, todėl „tonas“ gali nesutapti. Informacijos apie „toną“ nėra gamintojo kataloguose. Spalva nurodoma tik ant pakuočių, todėl rekomenduojame ją išsaugoti.<br><br>
                
Jei gamintojo kataloge nepažymėta, kad vienos kolekcijos plytelės ir dekorai yra vieno kalibro, tai jų dydžiai tarpusavyje gali truputį skirtis. Tai sudėtingo plytelių gamybos proceso rezultatas. Aukštoje temperatūroje plytelių masė plečiasi nevienodai, todėl plytelių dydis (kalibras) tiksliai nustatomas tik baigus gamybos procesą. Taip pat svarbu žinoti, kad yra leistini ir to paties kalibro plytelių + – 0,5% dydžio svyravimai (pagal darniojo Europos standarto EN14411 reikalavimus).
            
