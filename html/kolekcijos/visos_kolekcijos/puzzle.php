                        <h3>„Puzzle“ plytelės sienoms ir grindims</h3><br>
                        <p>1996 m. Edward Barber ir Jay Osgerby atidarė savo studiją Londone. Daugiadisciplininis jų požiūris sustiprino industrinio dizaino, architektūros ir dailės reikšmę. Jų darbai sudaro: „Knoll“, „Vitra“, „B & B Italia“, „Cappellini“, „Venini“ ir „Flos“ kolekcijas; ribotos spaudos leidinius, tokius kaip 2012 m. „Olimpinės lemputės“ Londone ir „Royal Mint“ sukurta 2 svarų sterlingų moneta. 2011 m. „Rizzoli New York“ paskelbė pirmąją jų monografiją. Edward Barber ir Jay Osgerby yra karališkieji pramonės dizaineriai (RDI) ir garbės daktarai. Jų darbai yra įtraukti į nuolatines muziejų kolekcijas visame pasaulyje, pvz.: V & A ir dizaino muziejuje Londone, Metropoliteno meno muziejuje Niujorke ir Čikagos meno institute.<br><br>

2013 m. jie prisijungė prie MUTINA komandos su plytelių kolekcija „Mews“. 2016 m. atnaujino bendradarbiavimą su nauja kolekcija „Puzzle“.<br><br>

 

„Puzzle“ tai unikali spalvų ir geometrinių elementų kompozicija. Žaidžiant spalvomis ir jų gradacija, ši kolekcija leidžia sukurti grafinį vaizdą grindims ar sienoms.<br><br>

Šių glazūruotų akmens masės plytelių formatas yra: 25×25. Taip pat šios grindų ir sienų plytelės yra 14 mm storio, tai suteikia šioms plytelėms didesnę vertę.<br><br>

„Puzzle“ sienų ir grindų plytelių seriją sudaro 8 spalvos, kurios turi:<br><br>

                       
1.šešių grafinių modelių kompozicijas<br>
2.trijų baspalvų bazines (vienspalves) plyteles<br>
3.dviejų spalvų simetriško rašto plyteles<br><br>

                            
