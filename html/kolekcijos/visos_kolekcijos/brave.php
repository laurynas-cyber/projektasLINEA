                        <h3>„Brave“ plytelių kolekcija</h3><br>
                        <p>,,Brave“ – akmens masės grindų ir baltojo molio sieninių plytelių kolekcija, perteikianti retą natūralaus akmens amžinojo grožio estetiką. Grindų ir sienų plytelių paviršių asortimentas puikiai atkuria penkių skirtingų akmenų spalvas. Šios turtingos raukšlėtu paviršiumi, gyslomis ir kitais natūralaus akmens fragmentais plytelės, leis kurti amžinus ir unikalius interjerus. „Brave“ kolekciją papildo 3D efekto plytelių raštas.<br><br>

Taip pat ši kolekcija gali pasiūlyti šiurkštaus paviršiaus ir lauko terasoms skirtas 2 cm storio plyteles.<br><br>

Ypatingai didelis dėmesys šioje kolekcijoje skiriamas mozaikoms. Jų yra pačių įvairiausių: paprastų kvadračiukų, kurie naudojami dušo kabinų grindims arba kaip dekorinis elementas, taip pat 3D mozaikų, kurių pailgos detalės yra suklijuotos skirtingais aukščiais. Pastarąsias mozaikas, kaip dekorą, galima naudoti tik sienoms.<br><br>

Grindų plytelių matmenys: 30×60; 60×60; 45×90; 75×75; 75×150 cm.<br><br>

 

Sieninių plytelių matmenys: 40×80 cm.<br><br>

Plytelių storiai: 10; 8,5; 20 mm.<br><br>

Plytelių slidumo klasė: R10; R11.<br><br>

